    private void jButton21ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton21ActionPerformed
        // TODO add your handling code here:

        int index = jListRSS.getSelectedIndex();
        if(index==-1) return;
        RSSLink rssLink = parent.settings.feedList.remove(index);

        rssLink.description = jTextCategory0.getText();
        rssLink.url = jTextURL0.getText();

        if(rssLink.description.equals("")) { rssLink.description = "UNDEFINED"; }
        if(!rssLink.url.startsWith("http://")) { rssLink.url = "http://" + rssLink; }
        parent.settings.feedList.add(index,rssLink);

        refreshList();
        new MessageBox(this,"Settings Updated!").setVisible(true);
        jListRSS.setSelectedIndex(index);
        
    }//GEN-LAST:event_jButton21ActionPerformed

