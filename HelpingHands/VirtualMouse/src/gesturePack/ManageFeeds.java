    private void jListRSSValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_jListRSSValueChanged
// TODO add your handling code here:
        int index = jListRSS.getSelectedIndex();
        if(index==-1) return;
        RSSLink rssLink = parent.settings.feedList.get(index);
        
        jTextCategory0.setText(rssLink.description);
        jTextURL0.setText(rssLink.url);
        
    }//GEN-LAST:event_jListRSSValueChanged

