    public void refreshList() {
        lmRSS.clear();
        for(int i=0;i<parent.settings.feedList.size();i++) {
            RSSLink rssLink = parent.settings.feedList.get(i);
            String rss = rssLink.description + " >> " + rssLink.url;
            lmRSS.addElement(rss);
        }
        jListRSS.setModel(lmRSS);
    }
    
