    public void refreshList() {
        lmApp.clear();
        for(int i=0;i<parent.settings.appLinks.size();i++) {
            ApplicationLink appLink = parent.settings.appLinks.get(i);
            String app = appLink.command + " >> " + appLink.path;
            lmApp.addElement(app);
        }
        jListCommands.setModel(lmApp);
    }
    
