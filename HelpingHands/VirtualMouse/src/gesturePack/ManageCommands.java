    private void jListCommandsValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_jListCommandsValueChanged
// TODO add your handling code here:
        int index = jListCommands.getSelectedIndex();
        if(index==-1) return;
        ApplicationLink appLink = parent.settings.appLinks.get(index);
        
        jTextCommand.setText(appLink.command);
        jTextPath.setText(appLink.path);
        
        
    }//GEN-LAST:event_jListCommandsValueChanged

    private void jButton20ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton20ActionPerformed
// TODO add your handling code here:
        int index = jListCommands.getSelectedIndex();
        if(index==-1) return;
        parent.settings.appLinks.remove(index);
        refreshList();
        
    }//GEN-LAST:event_jButton20ActionPerformed

    private void jButton19ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton19ActionPerformed
// TODO add your handling code here:
        ApplicationLink appLink = new ApplicationLink();
        appLink.command = jTextCommand.getText();
        appLink.path = jTextPath.getText();
        if(appLink.command.equals("")) {
            return ;
        }
        parent.settings.appLinks.add(appLink);
        refreshList();
        new MessageBox(this,"Application Link Registered!").setVisible(true);
        
    }//GEN-LAST:event_jButton19ActionPerformed

    
